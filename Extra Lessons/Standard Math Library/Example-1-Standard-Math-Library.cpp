#include <iostream>
#include <math.h>
using namespace std;

int main()
{
    cout << "The cube of 8 is " << pow(8, 3) << endl; // pow (number, exponent value)

    cout << "The square-root of 8 is " << sqrt(8) << endl;
    cout << "The square-root of 8 is " << pow(8,0.5) << endl; // pow (number, exponent value)


    cout << "The cube-root of 8 is " << cbrt(8) << endl;
    cout << "The cube-root of 8 is " << pow(8, 0.33333333333) << endl; // pow (number, exponent value)

}